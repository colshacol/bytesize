export const whileHandler = () => {
	while (true) {
		console.log('hehehehe')
	}
}

export const forHandler = arr => {
	for (let i = 1; i > 0; i++) {
		console.log('hehehehe')
	}
}

export const forOfHandler = arr => {
	for (let bar of arr) {
		console.log('hehehehe')
	}
}

export const forInHandler = arr => {
	for (let bar in arr) {
		console.log('hehehehe')
	}
}
